LEFT JOIN REALTY_INCOME.RAW.TENANTS t ON l.TENANT_ID = t.TENANT_ID;

CREATE OR REPLACE VIEW FINANCIAL_TREND AS
SELECT
    fm.FISCAL_YEAR,
    fm.FISCAL_QUARTER,
    fm.REPORTING_DATE,
    COUNT(DISTINCT fm.PROPERTY_ID) AS PROPERTIES_REPORTING,
    SUM(fm.GROSS_REVENUE) AS TOTAL_REVENUE,
    SUM(fm.NET_OPERATING_INCOME) AS TOTAL_NOI,
    SUM(fm.OPERATING_EXPENSES) AS TOTAL_OPEX,
    SUM(fm.CAPITAL_EXPENDITURES) AS TOTAL_CAPEX,
    SUM(fm.FUNDS_FROM_OPERATIONS) AS TOTAL_FFO,
    SUM(fm.ADJUSTED_FFO) AS TOTAL_AFFO,
    AVG(fm.OCCUPANCY_RATE) AS AVG_OCCUPANCY,
    AVG(fm.RENT_COLLECTION_RATE) AS AVG_COLLECTION_RATE,
    AVG(fm.SAME_STORE_NOI_GROWTH) AS AVG_SS_NOI_GROWTH
FROM REALTY_INCOME.RAW.FINANCIAL_METRICS fm
GROUP BY fm.FISCAL_YEAR, fm.FISCAL_QUARTER, fm.REPORTING_DATE
ORDER BY fm.FISCAL_YEAR, fm.FISCAL_QUARTER;

CREATE OR REPLACE VIEW MAINTENANCE_SUMMARY AS
SELECT
    p.PROPERTY_ID,
    p.PROPERTY_NAME,
    p.PROPERTY_TYPE,
    p.STATE,
    m.MAINTENANCE_TYPE,
    m.STATUS AS MAINTENANCE_STATUS,
    m.PRIORITY,
    m.ESTIMATED_COST,
    m.ACTUAL_COST,
    m.SCHEDULED_DATE,
    m.COMPLETED_DATE,
    DATEDIFF('day', m.SCHEDULED_DATE, COALESCE(m.COMPLETED_DATE, CURRENT_DATE())) AS DAYS_TO_COMPLETE,
    CASE WHEN m.ACTUAL_COST > m.ESTIMATED_COST THEN 'Over Budget'
         WHEN m.ACTUAL_COST <= m.ESTIMATED_COST THEN 'On/Under Budget'
         ELSE 'Pending' END AS BUDGET_STATUS
FROM REALTY_INCOME.RAW.MAINTENANCE_RECORDS m
JOIN REALTY_INCOME.RAW.PROPERTIES p ON m.PROPERTY_ID = p.PROPERTY_ID;
