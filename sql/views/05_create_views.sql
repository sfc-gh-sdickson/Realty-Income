USE DATABASE REALTY_INCOME;
USE SCHEMA ANALYTICS;

CREATE OR REPLACE VIEW PROPERTY_PERFORMANCE AS
SELECT
    p.PROPERTY_ID,
    p.PROPERTY_NAME,
    p.PROPERTY_TYPE,
    p.SECTOR,
    p.CITY,
    p.STATE,
    p.SQUARE_FOOTAGE,
    p.YEAR_BUILT,
    p.YEAR_ACQUIRED,
    p.ACQUISITION_PRICE,
    p.CURRENT_VALUATION,
    p.CAP_RATE,
    p.OCCUPANCY_STATUS,
    t.TENANT_NAME,
    t.INDUSTRY,
    t.CREDIT_RATING,
    l.LEASE_TYPE,
    l.LEASE_START_DATE,
    l.LEASE_END_DATE,
    l.ANNUAL_RENT,
    l.RENT_ESCALATION_RATE,
    l.REMAINING_TERM_MONTHS,
    l.TRIPLE_NET,
    l.STATUS AS LEASE_STATUS,
    fm.FISCAL_YEAR,
    fm.FISCAL_QUARTER,
    fm.NET_OPERATING_INCOME,
    fm.OCCUPANCY_RATE,
    fm.SAME_STORE_NOI_GROWTH,
    fm.FUNDS_FROM_OPERATIONS
FROM REALTY_INCOME.RAW.PROPERTIES p
LEFT JOIN REALTY_INCOME.RAW.LEASES l ON p.PROPERTY_ID = l.PROPERTY_ID
LEFT JOIN REALTY_INCOME.RAW.TENANTS t ON l.TENANT_ID = t.TENANT_ID
LEFT JOIN REALTY_INCOME.RAW.FINANCIAL_METRICS fm ON p.PROPERTY_ID = fm.PROPERTY_ID;

CREATE OR REPLACE VIEW PORTFOLIO_HEALTH AS
