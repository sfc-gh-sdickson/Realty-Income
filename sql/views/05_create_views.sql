    ps.TOTAL_SQUARE_FOOTAGE,
    ps.PORTFOLIO_OCCUPANCY_RATE,
    ps.WEIGHTED_AVG_LEASE_TERM,
    ps.TOTAL_ANNUALIZED_RENT,
    ps.INVESTMENT_GRADE_TENANTS_PCT,
    ps.DIVERSIFICATION_SCORE,
    ps.TOTAL_DEBT,
    ps.DEBT_TO_EBITDA_RATIO,
    ps.INTEREST_COVERAGE_RATIO,
    ps.DIVIDEND_PER_SHARE,
    ps.FFO_PER_SHARE,
    ps.AFFO_PER_SHARE,
    ps.PAYOUT_RATIO,
    md.STOCK_PRICE,
    md.MARKET_CAP,
    md.DIVIDEND_YIELD,
    md.PRICE_TO_FFO,
    md.PRICE_TO_AFFO,
