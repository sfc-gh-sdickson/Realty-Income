    ON md.MARKET_DATE = ps.REPORTING_DATE;

CREATE OR REPLACE VIEW LEASE_EXPIRATION_SCHEDULE AS
SELECT
    YEAR(l.LEASE_END_DATE) AS EXPIRATION_YEAR,
    QUARTER(l.LEASE_END_DATE) AS EXPIRATION_QUARTER,
    COUNT(*) AS LEASE_COUNT,
    SUM(l.ANNUAL_RENT) AS TOTAL_RENT_EXPIRING,
    AVG(l.RENT_ESCALATION_RATE) AS AVG_ESCALATION_RATE,
    COUNT(CASE WHEN t.CREDIT_RATING IN ('AAA','AA+','AA','AA-','A+','A','A-','BBB+','BBB','BBB-') THEN 1 END) AS INVESTMENT_GRADE_COUNT,
    COUNT(CASE WHEN l.TRIPLE_NET = TRUE THEN 1 END) AS NNN_LEASE_COUNT
FROM REALTY_INCOME.RAW.LEASES l
LEFT JOIN REALTY_INCOME.RAW.TENANTS t ON l.TENANT_ID = t.TENANT_ID
WHERE l.STATUS = 'Active'
GROUP BY YEAR(l.LEASE_END_DATE), QUARTER(l.LEASE_END_DATE)
ORDER BY EXPIRATION_YEAR, EXPIRATION_QUARTER;

CREATE OR REPLACE VIEW TENANT_CONCENTRATION AS
SELECT
    t.TENANT_NAME,
    t.INDUSTRY,
    t.CREDIT_RATING,
    t.PUBLIC_COMPANY,
    COUNT(DISTINCT l.PROPERTY_ID) AS PROPERTY_COUNT,
    SUM(l.ANNUAL_RENT) AS TOTAL_ANNUAL_RENT,
    ROUND(SUM(l.ANNUAL_RENT) / NULLIF((SELECT SUM(ANNUAL_RENT) FROM REALTY_INCOME.RAW.LEASES WHERE STATUS = 'Active'), 0) * 100, 2) AS PCT_OF_TOTAL_RENT,
    AVG(l.REMAINING_TERM_MONTHS) AS AVG_REMAINING_TERM,
    MIN(l.LEASE_END_DATE) AS EARLIEST_EXPIRATION,
    MAX(l.LEASE_END_DATE) AS LATEST_EXPIRATION
FROM REALTY_INCOME.RAW.TENANTS t
JOIN REALTY_INCOME.RAW.LEASES l ON t.TENANT_ID = l.TENANT_ID
WHERE l.STATUS = 'Active'
GROUP BY t.TENANT_NAME, t.INDUSTRY, t.CREDIT_RATING, t.PUBLIC_COMPANY
ORDER BY TOTAL_ANNUAL_RENT DESC;

CREATE OR REPLACE VIEW GEOGRAPHIC_DISTRIBUTION AS
SELECT
    p.STATE,
    COUNT(*) AS PROPERTY_COUNT,
    SUM(p.SQUARE_FOOTAGE) AS TOTAL_SQFT,
    AVG(p.CAP_RATE) AS AVG_CAP_RATE,
    SUM(p.CURRENT_VALUATION) AS TOTAL_VALUATION,
    COUNT(CASE WHEN p.OCCUPANCY_STATUS = 'Occupied' THEN 1 END)::FLOAT / COUNT(*) * 100 AS OCCUPANCY_PCT,
    SUM(l.ANNUAL_RENT) AS TOTAL_ANNUAL_RENT,
    AVG(r.RISK_SCORE) AS AVG_RISK_SCORE
FROM REALTY_INCOME.RAW.PROPERTIES p
LEFT JOIN REALTY_INCOME.RAW.LEASES l ON p.PROPERTY_ID = l.PROPERTY_ID
LEFT JOIN REALTY_INCOME.RAW.RISK_ASSESSMENTS r ON p.PROPERTY_ID = r.PROPERTY_ID
GROUP BY p.STATE
ORDER BY PROPERTY_COUNT DESC;

CREATE OR REPLACE VIEW RISK_DASHBOARD AS
